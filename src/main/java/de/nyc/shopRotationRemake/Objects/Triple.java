package de.nyc.shopRotationRemake.Objects;

public class Triple {
    private String value1;
    private String value2;
    private String value3;

    public Triple(String value1, String value2, String value3) {
        this.value1 = value1;
        this.value2 = value2;
        this.value3 = value3;
    }

    // Getters
    public String getValue1() {
        return value1;
    }

    public String getValue2() {
        return value2;
    }

    public String getValue3() {
        return value3;
    }

    @Override
    public String toString() {
        return "Triple{" +
                "value1='" + value1 + '\'' +
                ", value2='" + value2 + '\'' +
                ", value3='" + value3 + '\'' +
                '}';
    }
}
